void app_main(void)
{
    printf("Hello world!\n");

    /* Print chip information */
    esp_chip_info_t chip_info;
    uint32_t flash_size;
    esp_chip_info(&chip_info);
    printf("This is %s chip with %d CPU core(s), %s%s%s%s, ",
           CONFIG_IDF_TARGET,
           chip_info.cores,
           (chip_info.features & CHIP_FEATURE_WIFI_BGN) ? "WiFi/" : "",
           (chip_info.features & CHIP_FEATURE_BT) ? "BT" : "",
           (chip_info.features & CHIP_FEATURE_BLE) ? "BLE" : "",
           (chip_info.features & CHIP_FEATURE_IEEE802154) ? ", 802.15.4 (Zigbee/Thread)" : "");

    unsigned major_rev = chip_info.revision / 100;
    unsigned minor_rev = chip_info.revision % 100;
    printf("silicon revision v%d.%d, ", major_rev, minor_rev);
    if(esp_flash_get_size(NULL, &flash_size) != ESP_OK) {
        printf("Get flash size failed");
        return;
    }

    printf("%" PRIu32 "MB %s flash\n", flash_size / (uint32_t)(1024 * 1024),
           (chip_info.features & CHIP_FEATURE_EMB_FLASH) ? "embedded" : "external");

    printf("Minimum free heap size: %" PRIu32 " bytes\n", esp_get_minimum_free_heap_size());

    // Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    //init
    xCommandQueue = xQueueCreate(2, sizeof(CommandData_t));
    if (xCommandQueue == NULL) {
        ESP_LOGE(TAG, "Failed to create command queue");
        return;
    }

    xCameraQueue = xQueueCreate(1, sizeof(int));
    if (xCameraQueue == NULL) {
        ESP_LOGE(TAG, "Failed to create camera queue");
        return;
    }

    initialise_wifi();

    #ifdef CONFIG_EXAMPLE_IPV4
    xTaskCreate(tcp_server_task_8080, "tcp_server_8080", 8192, (void*)AF_INET, 20, NULL);
    xTaskCreate(tcp_server_task_8081, "tcp_server_8081", 8192, (void*)AF_INET, 20, NULL);
    #endif
    #ifdef CONFIG_EXAMPLE_IPV6
    xTaskCreate(tcp_server_task, "tcp_server", 8192, (void*)AF_INET6, 20, NULL);
    #endif

    //peripherals init
    xTaskCreate(vTaskProcessCmd, "ProcessCmd", 8192, NULL, 15, &xTaskProcessCmd);
    xTaskCreate(vTaskCamera, "Camera", 8192, NULL, 10, &xTaskCamera);

    while(1) {
        
        vTaskDelay(1000);
    }
}

void vTaskProcessCmd(void *pvParameters) 
{
    CommandData_t cmdData;
    while (1) {
        if (xQueueReceive(xCommandQueue, &cmdData, portMAX_DELAY) == pdPASS) {
            // 处理命令
            char *response = handle_command(cmdData.command);
            ESP_LOGI(TAG, "Response: %s", response);

            // 将响应发送回客户端
            int to_write = strlen(response);
            while (to_write > 0) {
                int written = send(cmdData.sock, response + (strlen(response) - to_write), to_write, 0);
                if (written < 0) {
                    ESP_LOGE(TAG, "Error occurred during sending: errno %d", errno);
                    break;
                }
                to_write -= written;
            }
        }
    }
}
 
